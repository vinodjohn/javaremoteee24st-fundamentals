package org.sda;

/**
 * Calculator to do all arithmetic operations
 *
 * @author Vinod John
 * @Date 04.02.2023
 */
public class Calculator {
    public float addTwoNumbers(float a, float b) {
        return a + b;
    }

    public float multiplyTwoNumbers(float a, float b) {
        return a * b;
    }
}
